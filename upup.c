#include "printf.h"

char	*upup(char *c)
{
	int	i;

	i = 0;
	while (c[i])
	{
		if (c[i] >= 'a' && c[i] <= 'z')
			c[i] = c[i] + 32;
		i++;
	}
	return (c);
}
