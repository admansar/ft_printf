#include "printf.h"

void ft_putnbr_ui(unsigned int nb)
{
	char a;

	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		a = '0' + (nb % 10);
		ft_putchar(a);
	}
}

